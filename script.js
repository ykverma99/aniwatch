let trendingCards = document.getElementById("box2_lists");

function trendingCardsList() {
  trendingData.forEach((elm, i) => {
    const card = document.createElement("div");
    card.classList.add("list_card");
    const cardNumber = document.createElement("p");
    cardNumber.classList.add("list_card--heading");
    cardNumber.textContent = i < 10 ? `0${i + 1}` : i;
    i < 3
      ? cardNumber.classList.add("list_card--heading-border")
      : cardNumber.classList.add("list_card--heading-dull");
    const cardImgContainer = document.createElement("div");
    cardImgContainer.classList.add("list_card--img");
    const cardImg = document.createElement("img");
    cardImg.src = elm.img;
    cardImg.addEventListener("mouseenter", () => {
      cardImgContainer.appendChild(glassContainer(elm.title, elm.para));
    });
    cardImg.addEventListener("mouseleave", () => {
      const glass = document.querySelector(".glass");
      if (glass) {
        glass.remove();
      }
    });
    const cardInfo = document.createElement("div");
    cardInfo.classList.add("list_card_info");
    const cardHeading = document.createElement("p");
    cardHeading.classList.add("list_card_info--heading");
    cardHeading.textContent = elm.title;
    const cardDetail = document.createElement("div");
    cardDetail.classList.add("info_options");
    elm.options.forEach((val, i) => {
      const cardOption = document.createElement("div");
      cardOption.classList.add("all_option");
      if (i % 2 == 0) {
        cardOption.classList.add("start_left-radius");
        cardOption.classList.add("green");
      } else {
        cardOption.classList.add("end_right-radius");
        cardOption.classList.add("pink");
      }
      const optionImg = document.createElement("img");
      optionImg.src = val.img;
      const optionTxt = document.createElement("p");
      optionTxt.textContent = val.txt;
      cardOption.appendChild(optionImg);
      cardOption.appendChild(optionTxt);
      cardDetail.appendChild(cardOption);
    });
    cardImgContainer.appendChild(cardImg);
    card.appendChild(cardNumber);
    card.appendChild(cardImgContainer);
    cardInfo.appendChild(cardHeading);
    cardInfo.appendChild(cardDetail);
    card.appendChild(cardInfo);
    trendingCards.appendChild(card);
  });
}

