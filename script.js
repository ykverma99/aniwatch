function glassContainer(heading, para) {
  const glass = document.createElement("div");
  glass.classList.add("glass");
  const glassHeading = document.createElement("p");
  glassHeading.classList.add("glass_heading");
  glassHeading.textContent = heading;
  const glassDetail = document.createElement("div");
  glassDetail.classList.add("glass_detail");
  const detail_1 = document.createElement("span");
  detail_1.classList.add(`detail_1`);
  const detail_2 = document.createElement("span");
  detail_2.classList.add(`detail_2`);
  const detail_3 = document.createElement("div");
  detail_3.classList.add(`detail_3`);
  detail_3.classList.add("yellow");
  detail_3.classList.add("btn");
  detail_3.textContent = "TV";
  const detail_1Img = document.createElement("img");
  detail_1Img.src = "./images/star.svg";
  const detail_1Para = document.createElement("p");
  detail_1Para.textContent = "6.4";
  const detail_2Text_1 = document.createElement("p");
  detail_2Text_1.classList.add("btn");
  detail_2Text_1.classList.add("yellow");
  detail_2Text_1.classList.add("start_left-radius");
  detail_2Text_1.textContent = "HD";
  const detail_2Img = document.createElement("div");
  detail_2Img.classList.add("btn");
  detail_2Img.classList.add("green");
  detail_2Img.classList.add("end_right-radius");
  const detail_2_img = document.createElement("img");
  detail_2_img.src = "./images/caption.svg";
  const detail_2Text_2 = document.createElement("p");
  detail_2Text_2.textContent = "3";
  // appending detail section
  detail_2Img.append(detail_2_img, detail_2Text_2);
  detail_2.append(detail_2Text_1, detail_2Img);
  detail_1.append(detail_1Img, detail_1Para);
  glassDetail.append(detail_1, detail_2, detail_3);

  // glass para
  const glassPara = document.createElement("p");
  glassPara.classList.add("glass_para");
  glassPara.textContent = para;

  // glass buttons

  const glasssButtons = document.createElement("div");
  glasssButtons.classList.add("glass_btns");
  const glassBtn = document.createElement("button");
  glassBtn.classList.add("glass_btn");
  const glassBtnImg = document.createElement("img");
  glassBtnImg.src = "./images/play_copy.svg";
  glassBtn.appendChild(glassBtnImg);
  glassBtn.textContent = "Watch now";
  const glassAdd = document.createElement("div");
  glassAdd.classList.add("glass_add");
  const glassAddImg = document.createElement("img");
  glassAddImg.src = "./images/add.svg";
  glassAdd.appendChild(glassAddImg);
  glasssButtons.append(glassBtn, glassAdd);
  glass.append(glassHeading, glassDetail, glassPara, glasssButtons);
  return glass;
}

